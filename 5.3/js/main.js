// Дано ціле число N (ввести через prompt). Вивести всі цілі числа від 1 до 100, квадрат яких не перевищує числа N

let N = parseInt(prompt("Введите целое число N: "), 10);

if (isNaN(N)) {
    console.log("Пожалуйста, введите допустимое целое число.");
} else {
    for (let i = 1; i <= 100; i++) {
        if (i * i <= N) {
            console.log(i);
        }
    }
}